package com.bean;

public class Pix {
    private double x;
    private double y;

    public Pix(double xp, double yp) {
        this.x = xp;
        this.y = yp;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
