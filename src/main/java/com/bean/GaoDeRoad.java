package com.bean;

public class GaoDeRoad {

    private String key;
    private XYZ Xyz;
    private Object building;
    private Object region;
    private Object road;


    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public XYZ getXyz() {
        return Xyz;
    }

    public void setXyz(XYZ xyz) {
        Xyz = xyz;
    }

    public Object getBuilding() {
        return building;
    }

    public void setBuilding(Object building) {
        this.building = building;
    }

    public Object getRegion() {
        return region;
    }

    public void setRegion(Object region) {
        this.region = region;
    }

    public Object getRoad() {
        return road;
    }

    public void setRoad(Object road) {
        this.road = road;
    }
}
